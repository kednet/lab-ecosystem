def score_to_verdict(score: float) -> dict:
    if score < 0.20:
        return VERDICTS[0]
    if score < 0.35:
        return VERDICTS[1]
    if score < 0.50:
        return VERDICTS[2]
    if score < 0.65:
        return VERDICTS[3]
    if score < 0.80:
        return VERDICTS[4]
    return VERDICTS[5]


def score_to_imposed_pct(score: float) -> int:
    """0..1 score → 0..100% навязанного (инверсия)."""
    return round((1.0 - max(0.0, min(1.0, score))) * 100)


# === Состояние сессии (для одного пользователя) ===

@dataclass
class DetectorSession:
    user_id: int
    step: int = 0             # 0..5 (0 = интро ещё не показан, 1..5 = вопросы)
    answers: list[float] = None  # length = step (0..5)

    def __post_init__(self):
        if self.answers is None:
            self.answers = []

    @property
    def is_finished(self) -> bool:
        return self.step >= len(QUESTIONS)

    def record(self, value: float) -> None:
        self.answers.append(value)
        self.step += 1

    def compute(self) -> tuple[dict, int]:
        if not self.answers:
            return VERDICTS[2], 50  # fallback
        score = sum(self.answers) / len(self.answers)
        verdict = score_to_verdict(score)
        imposed_pct = score_to_imposed_pct(score)
        return verdict, imposed_pct


# === Форматирование (общее для TG и ВК, без HTML) ===

def format_intro() -> str:
    return (
        "🔍 <b>Детектор желаний</b>\n\n"
        "Твоё желание — про «надо» или про «хочу»?\n\n"
        "5 вопросов, 2 минуты. Без email, без регистрации.\n"
        "Поймёшь, насколько оно идёт изнутри — и сколько в нём социального шума.\n\n"
        "Нажми «Начать», чтобы пройти."
    )


def format_question(q: DetectorQuestion) -> str:
    lines = [f"<b>Вопрос {q.idx} из {len(QUESTIONS)}</b>", "", q.text, ""]
    for i, (label, _w) in enumerate(q.options):
        lines.append(f"{i+1}. {label}")
    return "\n".join(lines)


def format_result(verdict: dict, imposed_pct: int) -> str:
    lines = [
        "✨ <b>Твой результат</b>",
        "",
        f"<b>{imposed_pct}% навязанного</b>",
        "",
        f"<i>{verdict['label']}</i>",
        "",
        verdict["description"],
        "",
        "<b>Что с этим делать:</b>",
    ]
    for i, a in enumerate(verdict["advice"], 1):
        lines.append(f"{i}. {a}")
    return "\n".join(lines)


